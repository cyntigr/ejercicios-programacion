public class Ejercicio22{
  public static void main(String[] args) {
    System.out.println("Muestra los números primos entre 2 y 100 ambos incluidos.");
    System.out.println("---------------------------------------------------------");
    
    for (int i = 2; i <= 100; i+=2){
      System.out.print(i + " ");
      
    }
  }
}
